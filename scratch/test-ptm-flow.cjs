const { createClient } = require('@supabase/supabase-js');
const fs = require('fs');
const path = require('path');

const envPath = path.resolve(process.cwd(), '.env');
const envContent = fs.readFileSync(envPath, 'utf-8');
const env = {};
envContent.split('\n').forEach(line => {
  const parts = line.split('=');
  if (parts.length >= 2) {
    env[parts[0].trim()] = parts.slice(1).join('=').trim();
  }
});

const supabaseUrl = env['VITE_SUPABASE_URL'];
const supabaseServiceKey = env['VITE_SUPABASE_SERVICE_ROLE_KEY'];

const supabaseAdmin = createClient(supabaseUrl, supabaseServiceKey);

async function run() {
  console.log("==== STARTING PTM INTEGRATION TESTS ====");

  const teacherUserId = '38f8269e-fb13-4ca1-aada-a5c59e83417e'; // Vishal Yadav user_id
  const studentProfileId = '0a0c1519-61af-46dc-8c25-18e7e8dc0141'; // Manan Yadav student_id
  const schoolId = '0a5e1f1a-8a04-4b77-8215-a7ab9a48e342';
  
  // 1. Fetch teacher record
  const { data: teacher } = await supabaseAdmin
    .from('teachers')
    .select('id')
    .eq('user_id', teacherUserId)
    .single();
  console.log(`Teacher resolved ID: ${teacher.id}`);

  // 2. Fetch parent mapping (matching the frontend logic we fixed)
  const { data: mapping } = await supabaseAdmin
    .from('parent_student_mapping')
    .select('parent_id')
    .eq('student_id', studentProfileId)
    .maybeSingle();
  console.log(`Parent profile mapping: ${mapping ? mapping.parent_id : 'Not Found'}`);

  let parentUserId = '';
  if (mapping) {
    const { data: parentRecord } = await supabaseAdmin
      .from('parents')
      .select('user_id')
      .eq('id', mapping.parent_id)
      .maybeSingle();
    console.log(`Parent user ID resolved: ${parentRecord.user_id}`);
    parentUserId = parentRecord.user_id;
  }

  if (!parentUserId) {
    throw new Error("Unable to resolve parent user ID for the selected student!");
  }

  // 3. Delete any previous test PTMs to start clean
  await supabaseAdmin
    .from('ptm_meetings')
    .delete()
    .eq('title', 'INTEGRATION_TEST_PTM');
  console.log("Deleted old test PTM records.");

  // 4. Create secure meet room code and link
  const meetCode = `PTM-TEST${Math.floor(1000 + Math.random() * 9000)}`;
  const link = `https://aegiserp.xyz/meet/${meetCode}`;

  // 5. Build payload
  const ptmPayload = {
    school_id: schoolId,
    class_id: '03bdbf0b-77d1-455e-a7d3-e2f2a638bb34', // Class of student
    student_id: studentProfileId,
    parent_id: parentUserId,
    teacher_id: teacher.id,
    title: 'INTEGRATION_TEST_PTM',
    description: 'This is a test meeting generated by our automated integration suite.',
    meeting_mode: 'ONLINE',
    meeting_link: link,
    scheduled_date: '2026-06-25',
    start_time: '14:00:00',
    end_time: '14:30:00',
    status: 'SCHEDULED'
  };

  // 6. Insert meeting row
  console.log("Inserting PTM meeting row...");
  const { data: insertedPTM, error: ptmErr } = await supabaseAdmin
    .from('ptm_meetings')
    .insert(ptmPayload)
    .select('*')
    .single();

  if (ptmErr) {
    console.error("PTM Insert failed:", ptmErr);
    throw ptmErr;
  }
  console.log(`PTM scheduled successfully! ID: ${insertedPTM.id}, link: ${insertedPTM.meeting_link}`);

  // 7. Insert attendance row
  const { data: attendanceRow, error: attErr } = await supabaseAdmin
    .from('ptm_attendance')
    .insert({ meeting_id: insertedPTM.id })
    .select('*')
    .single();
  if (attErr) {
    console.error("Attendance creation failed:", attErr);
    throw attErr;
  }
  console.log("PTM Attendance record auto-created.");

  // 8. Insert Notification records (For parent, student, and teacher)
  const notificationsPayload = [
    { user_id: parentUserId, meeting_id: insertedPTM.id, title: 'New PTM Scheduled', message: 'Test message for parent' },
    { user_id: '58adfee0-aacb-4900-9246-a1364ca01750', meeting_id: insertedPTM.id, title: 'New PTM Scheduled', message: 'Test message for student' },
    { user_id: teacherUserId, meeting_id: insertedPTM.id, title: 'PTM Scheduled Successfully', message: 'Test message for teacher' }
  ];

  const { error: notifErr } = await supabaseAdmin
    .from('ptm_notifications')
    .insert(notificationsPayload);
  if (notifErr) {
    console.error("Notifications creation failed:", notifErr);
    throw notifErr;
  }
  console.log("PTM notifications created for Parent, Student, and Teacher.");

  // 9. Query notification bell table check
  const { data: bells } = await supabaseAdmin
    .from('ptm_notifications')
    .select('*')
    .eq('meeting_id', insertedPTM.id);
  console.log(`Verified ptm_notifications entries count: ${bells.length}`);

  console.log("==== PTM INTEGRATION TESTS PASSED SUCCESSFULLY ====");
}

run().catch(err => {
  console.error("PTM integration test run failed:", err);
  process.exit(1);
});
